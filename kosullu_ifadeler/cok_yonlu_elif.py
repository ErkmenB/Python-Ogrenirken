deger = int(input("0 ile 5 arasında bir değer giriniz : "))

if deger < 0:
    print("Çok küçük bir değer girdiniz")
elif deger == 0:
    print("sifir")
elif deger == 1:
    print("bir")
elif deger == 2 :
    print("2")
elif deger == 3 :
    print("üç")
elif deger == 4 :
    print("dört")
elif deger == 5 :
    print("beş")
elif deger > 5 :
    print("Çok büyük bir değer girdiniz")